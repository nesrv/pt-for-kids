# Урок 1: Класс Point — что такое класс и объект
#
# Класс — чертёж. Объект — конкретная вещь, сделанная по чертежу.
# Point(3, 5) — точка с координатами x=3, y=5.


class Point:
    """Точка на плоскости с координатами x и y."""

    def __init__(self, x, y):
        # self — это сама точка; сохраняем координаты внутри неё
        self.x = x
        self.y = y


# --- программа ---

# создаём два объекта (две разные точки)
a = Point(3, 5)
b = Point(-2, 7)

print("Точка a:", a.x, a.y)
print("Точка b:", b.x, b.y)

# у каждого объекта свои значения — они не мешают друг другу
b.x = 0
print("После изменения b.x:", b.x, b.y)

